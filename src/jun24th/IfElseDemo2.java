package jun24th;

public class IfElseDemo2 {

	public static void main(String[] args) {
		
		int x=30,y=30;
		
		if(x>y)
		{
			System.out.println(" x is greater than y");
		}
		else if(x<y)
		{
			System.out.println(" x is less than y");
		}
		else if(x==y)
		{
			System.out.println(" x is equals to y");
		}

	}

}
