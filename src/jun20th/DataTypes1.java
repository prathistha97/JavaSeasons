package jun20th;

public class DataTypes1 {

	public static void main(String[] args) {
		
		byte b=-128;
		
		short s=-32768;
		
		int i=-2147483648;
		
		long l=-128L;
		
		float f=3.14f;
		
		double d=3.14;
		
		char c='a';
		
		System.out.println(b);
		
		System.out.println(s);
		
		System.out.println(i);
		
		System.out.println(l);
		
		System.out.println(f);
		
		System.out.println(d);
		
		System.out.println(c);

	}

}
